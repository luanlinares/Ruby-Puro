#Maneiras de criar um Regex

#1 - Utilizando o formato /…/
  /expressao/
#===================================
#2 - Através do formato %r{…}
  #%r{expressao}
#===================================
#3 - Utilizando um Construtor através do Regexp.new(‘…’)
  #Regexp.new('expressao'
